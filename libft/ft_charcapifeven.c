char	ft_charcapifeven(unsigned int i, char c)
{
	if (i % 2 == 0 && c >= 'a' && c <= 'z')
		return (c - 32);
	if (i % 2 == 1 && c >= 'A' && c <= 'Z')
		return (c + 32);
	return (c);
}
